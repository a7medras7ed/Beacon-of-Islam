function openSection(section) {
    const container = document.getElementById("azkar");
    container.innerHTML = "";
    container.style.display = "block";

    azkarData[section].forEach((item, index) => {
        container.innerHTML += `
            <div class="zekr-box">
                <div class="zekr-text">${item.text}</div>
                <div class="repeat-box">
                    <span>التكرار: <b id="count-${index}">${item.count}</b></span>
                    <button class="repeat-btn" onclick="reduce(${index})">تم</button>
                </div>
            </div>
        `;
    });
}

function reduce(i) {
    const num = document.getElementById(`count-${i}`);
    let current = parseInt(num.innerText);
    if (current > 0) {
        num.innerText = current - 1;
    }
}
