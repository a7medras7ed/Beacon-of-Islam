// الحصول على رقم السورة من الرابط
const urlParams = new URLSearchParams(window.location.search);
const suraNumber = urlParams.get("sura");

const suraNameElement = document.getElementById("sura-name");
const suraTextElement = document.getElementById("sura-text");

// جلب اسم السورة
suraNameElement.innerText = "سورة " + suraList[suraNumber - 1];

// جلب الآيات من API
fetch(`https://api.alquran.cloud/v1/surah/${suraNumber}`)
    .then(res => res.json())
    .then(data => {
        let verses = data.data.ayahs;

        let html = "";
        verses.forEach(v => {
            html += `${v.text} ۝ `;
        });

        suraTextElement.innerHTML = html;
    });
